#include "minishell.h"

int	exec_pwd(void)
{
	char	cwd[1000];

	if (getcwd(cwd, 1000))
	{
		ft_putendl_fd(cwd, 1);
		g_var.exit_code = "0";
		return (1);
	}
	else
		return (-1);
}
